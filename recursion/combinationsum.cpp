// यह पैटर्न बैकट्रैकिंग या पुनरावृत्त खोज (recursive exploration) का है। इसमें हम एक-एक करके सभी संभावित विकल्पों को आजमाते हैं, अगर रास्ता सही दिशा में लगता है, तो आगे बढ़ते हैं। जैसे ही कोई रास्ता गलत हो जाए, हम वापस लौटते हैं (यानी बैकट्रैक करते हैं)। यह तरीका तब इस्तेमाल होता है जब हमें सभी संभावित समाधान खोजने होते हैं, जैसे कॉम्बिनेशन, परमीटेशन, या पजल जैसे न-क्वींस या सुडोकू।

// "Combination Sum" सवाल में होता यह है कि हमें कुछ नंबर दिए होते हैं (जिन्हें हम बार-बार इस्तेमाल कर सकते हैं) और हमें उनका ऐसा कॉम्बिनेशन ढूँढना होता है, जिनका कुल योग हमारे टार्गेट के बराबर हो। हमें सारी संभावित यूनिक कॉम्बिनेशन लिस्ट करनी होती हैं—जैसे कि अगर आपके पास 2, 3, 6, 7 हैं और टार्गेट 7 है, तो 2+2+3 या सिर्फ 7 एक कॉम्बिनेशन हो सकता है। बस, इस तरह हमें सभी सही कॉम्बिनेशन निकालने होते हैं!


class Solution {
public:
// इसका तरीका यह है कि हम हर नंबर पर दो विकल्प देखते हैं: उसे चुनें या छोड़ें। अगर चुनते हैं, तो टार्गेट से उसे घटाकर आगे बढ़ते हैं, और उसी नंबर को फिर से चुनने का मौका रहता है। अगर टार्गेट शून्य हो जाए, तो वो एक सही संयोजन है। अगर टार्गेट शून्य से कम हो जाए, तो हम वापस लौटते हैं। इस तरह हम सभी संभावनाएँ आज़माकर सही कॉम्बिनेशन निकाल लेते हैं।

// कोड में सबसे पहले हम एक फ़ंक्शन बनाते हैं जो हमारी सारी कॉम्बिनेशन जमा करेगा। फिर हम हर नंबर पर लूप चलाते हैं। अगर हम एक नंबर चुनते हैं, तो हम टार्गेट में से वही नंबर घटाकर उसी नंबर से आगे फिर कोशिश करते हैं। अगर टार्गेट पूरा हो जाए तो हम उस कॉम्बिनेशन को रख लेते हैं। नहीं तो वापस लौटते हैं। इस तरह हम हर संभव रास्ता चेक करके सभी सही कॉम्बिनेशन हासिल करते हैं।
    void solve(int index, vector<int>& candidates, int target,
               vector<int>& temp, vector<vector<int>>& ans) {
        if (target == 0) {
            ans.push_back(temp);
            return;
        }

        if (target < 0)
            return;

            // बिल्कुल, यह जो फॉर लूप है, वो हर कैंडिडेट नंबर को एक-एक करके देखता है। जब हम एक नंबर चुनते हैं, तो उसे हम टेम्प नाम की लिस्ट में डालते हैं, ताकि हमारा अब तक का कॉम्बिनेशन बनता रहे। फिर हम उसी नंबर के साथ आगे बढ़ते हैं, क्योंकि एक नंबर को बार-बार इस्तेमाल करने की अनुमति है। इसके बाद, जब हम उस रास्ते को पूरा देख लेते हैं (चाहे वो सही निकले या नहीं), तो हम उस नंबर को टेम्प से हटा देते हैं (यानी पॉप बैक करते हैं), ताकि अगली कोशिश में वही कॉम्बिनेशन साफ-सुथरा हो, और हम अगला नंबर आजमा सकें।

        for (int i = index; i < candidates.size(); i++) {

            temp.push_back(candidates[i]);

            solve(i, candidates, target - candidates[i], temp, ans);

            temp.pop_back();
        }
    }

    vector<vector<int>> combinationSum(vector<int>& candidates, int target) {
        vector<vector<int>> ans;
        vector<int> temp;
        solve(0, candidates, target, temp, ans);
        return ans;
    }
};